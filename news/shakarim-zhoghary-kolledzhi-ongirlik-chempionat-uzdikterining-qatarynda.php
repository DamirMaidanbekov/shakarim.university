    <p>Аталған чемпионатқа Шәкәрім жоғары колледжінің студенттері мен оқытушылары 12 түрлі құзыреттілік бойынша қатысып, өз білімдері мен кәсіби дағдыларын көпшілік назарына ұсынды. Олардың қатарында ветеринария, флористика, CAD инженерлік дизайн, бұлтты бағдарламалау, фото және бейне өңдеу т.б. мамандықтар бар.</p>

    <p>Шәкәрім жоғары колледжінің қатысушылары 8 құзыреттілік бойынша жүлделі орындарға ие болды. Атап айтсақ, Ветеринария мамандығы бойынша білім алып жатқан – Нұргелді Қуаныш І орынға ие болса,– Дильназ Бекболатова (Флористика) мен Ақниет Рамазанова (Мектепке дейінгі білім беру) ІІ орыннан көрінді. Ал Данил Третьяков (CAD инженерлік дизайн), Дәулет Аңсағанов (Бұлтты бағдарламалау), Гүлім Бекжанова (Фото және бейне өңдеу), Әсем Қуанышқызы (Бастауыш білім беру), Дана Исина (Дене шынықтыру және спорт) жүлделі ІІІ орынды еншіледі.</p>

