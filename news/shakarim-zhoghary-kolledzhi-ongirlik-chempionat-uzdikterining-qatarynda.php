    <p><strong>Жуырда Абай облысының техникалық және кәсіптік білім беру оқу орындарының студенттері арасында кәсіби шеберліктің үшінші өңірлік чемпионаты өткен болатын.</strong></p>

    <p>WorldSkills – жай ғана жарыс емес, жас мамандарға өз дағдыларын көрсетуге және өздерін танытуға жақсы мүмкіндік. Биылғы жылы чемпионат ел Президенті жариялаған «Жұмысшы мамандықтар жылы» аясында өтті. Бұл техникалық, кәсіптік білім беруді ілгерілету жолындағы маңызды іс-шара болды.</p>

